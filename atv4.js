// Faça uma função que receba 2 parâmetros, nome e idade e retorne uma string com o texto "Olá, meu nome é ${nome} e tenho {idade} anos."

function nomeIdade(nome, idade) {
    return `Olá, meu nome é ${nome} e tenho ${idade} anos.`
}
